
import math


def sum(a, b):
    return a + b


def sub(a, b):
    return a - b


def mul(a, b):
    return a * b


def div(a, b):
    return a / b


def pow(a, b):
    return a**b


def exp(x):
    return math.exp(x)


def log(x, base):
    return math.log(x, base)


def log2(x):
    return log(x, 2)
